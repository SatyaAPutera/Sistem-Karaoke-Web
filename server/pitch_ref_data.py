def get_pitch_ref(song_id: int) -> list:
    """Kembalikan daftar pitch reference untuk song_id tertentu."""
    return PITCH_REF_MAP.get(song_id, [])


def pitch_ref_to_json(song_id: int) -> str:
    """Konversi pitch reference ke JSON string untuk disimpan di DB."""
    import json
    data = get_pitch_ref(song_id)
    # Format DB: [(t_ms, hz), ...] — simpan sebagai array objek
    return json.dumps([{"t": ev["t"], "hz": ev["hz"]} for ev in data])

